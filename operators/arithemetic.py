# ARITHEMETIC OPERATORS -> Arithmetic operators are used with numeric values to perform common mathematical operations:


a = 50
b = 10

print(a + b) # sum
print(a - b) # sub
print(a * b) # multiply
print(a / b) # divide
print(a // b) # integer division
print(a ** b) # exponent
print(a % b) # modulas
